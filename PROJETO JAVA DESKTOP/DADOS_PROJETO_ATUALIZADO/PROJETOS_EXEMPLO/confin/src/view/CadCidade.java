    private void atualizaTela() {
        EstadoDB estadodb = new EstadoDB();
        cbEstado.setModel(estadodb.getComboEstado());

        CidadeDB cidadedb = new CidadeDB();
        ArrayList<Cidade> listaCidade = cidadedb.getTodos();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nome");
        modelo.addColumn("Estado");
        for (Cidade auxCidade : listaCidade) {
            Object[] objetos = {auxCidade.getCid_codigo(), auxCidade.getNome(), auxCidade.getEst_sigla()};
            modelo.addRow(objetos);
            //JOptionPane.showMessageDialog(null, "Nome: "+auxCidade.getNome());
        }
        tbGrid.setModel(modelo);
        int linhas = tbGrid.getRowCount();
        if (linhas > 0) {
            tbGrid.setRowSelectionInterval(0, 0);
            Object auxCodigo = tbGrid.getValueAt(0, 0);
            Object auxNome = tbGrid.getValueAt(0, 1);
            Object auxEstado = tbGrid.getValueAt(0, 2);
            edtCodigo.setText(auxCodigo.toString());
            edtNome.setText(auxNome.toString());
            selecionaComboEstado(auxEstado.toString());
        }
    }

