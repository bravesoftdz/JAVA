    private void selecionaComboEstado(String sigla) {
        int indice = 0;
        for (int i = 0; i < cbEstado.getItemCount(); i++) {
            String auxSigla = cbEstado.getItemAt(i).toString();
            if (sigla.equals(auxSigla)) {
                indice = i;
                break;
            }
        }
        cbEstado.setSelectedIndex(indice);
    }

